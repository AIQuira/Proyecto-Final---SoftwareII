    private void iniciarlizarTabla() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nombre");      
        model.addColumn("Titulo Universitario");
        this.jTableListadoRevisores.setModel(model);
    }

    private void llenarTabla() {
        DefaultTableModel model = (DefaultTableModel) this.jTableListadoRevisores.getModel();
        limpiarTabla(); 
        ArrayList<Revisor> listaRevisores = (ArrayList<Revisor>) this.servicioRevisor.listarRevisores();

        if (listaRevisores.isEmpty()) {
            // Si no hay conferencias, muestra un mensaje en el JLabel
            txtCabecera.setText("En este momento no hay revisores.");
        } else {
            // Si hay conferencias, actualiza el texto
            txtCabecera.setText("Haz doble click en la conferencia para asignarle un revisor.");
            for (Revisor revisor : listaRevisores) {
                String[] fila = {    
                    revisor.getNombre(),      
                    revisor.getTituloUniversitario() // Pregunta: tÃ­tulo-disponibilidad
                };
                model.addRow(fila);
            }
        }
    }

    public void limpiarTabla(){
        DefaultTableModel modelo=(DefaultTableModel) this.jTableListadoRevisores.getModel();
        int filas=this.jTableListadoRevisores.getRowCount();
        for (int i = 0;filas>i; i++) {
            modelo.removeRow(0);
        }
    }

